print("This program will calculate an average from five numbers which you input.")
integer_1 = int(input("Please input your first integer: "))
integer_2 = int(input("Please input your second integer: "))
integer_3 = int(input("Please input your third integer: "))
integer_4 = int(input("Please input your fourth integer: "))
integer_5 = int(input("Please input your fifth integer: "))
result = (integer_1 + integer_2 + integer_3 + integer_4 + integer_5) / 5
print("The average of your numbers is {0}.".format(result))
